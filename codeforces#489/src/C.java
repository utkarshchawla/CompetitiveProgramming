import java.io.*;
import java.math.BigInteger;
import java.util.InputMismatchException;
import java.util.StringTokenizer;

public class C {
    static class FastWriter {
        private final BufferedWriter bw;

        public FastWriter() {
            this.bw = new BufferedWriter(new OutputStreamWriter(System.out));
        }

        public void print(Object object) throws IOException {
            bw.append("" + object);
        }

        public void println(Object object) throws IOException {
            print(object);
            bw.append("\n");
        }

        public void close() throws IOException {
            bw.close();
        }
    }

    static class FastReader {
        BufferedReader br;
        StringTokenizer st;

        public FastReader() {
            br = new BufferedReader(new InputStreamReader(System.in));
        }

        String next() {
            while (st == null || !st.hasMoreElements()) {
                try {
                    st = new StringTokenizer(br.readLine());
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
            return st.nextToken();
        }

        int nextInt() {
            return Integer.parseInt(next());
        }

        long nextLong() {
            return Long.parseLong(next());
        }

        double nextDouble() {
            return Double.parseDouble(next());
        }

        String nextLine() {
            String str = "";
            try {
                str = br.readLine();
            } catch (IOException e) {
                e.printStackTrace();
            }
            return str;
        }

        BigInteger nextBigInteger() {
            try {
                return new BigInteger(nextLine());
            } catch (NumberFormatException e) {
                throw new InputMismatchException();
            }
        }
    }

    public static void main(String[] args) {
        FastReader fr = new FastReader();
        FastWriter fw = new FastWriter();
        long x = fr.nextLong();
        long k = fr.nextLong();
        if (x == 0) {
            System.out.println(0);
            return;
        }
        BigInteger two = BigInteger.valueOf(2);
        BigInteger mod = BigInteger.valueOf(1000000007);
        BigInteger a = two.modPow(BigInteger.valueOf(k + 1), mod);
        BigInteger b = two.modPow(BigInteger.valueOf(k), mod);
        a = a.multiply(BigInteger.valueOf(x)).mod(mod);
        a = a.subtract(b).add(BigInteger.ONE).add(mod).mod(mod);
        int ans = a.intValue();
        if (ans <= 0) System.out.println(0);
        else System.out.println(ans);
    }
}
